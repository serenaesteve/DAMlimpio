
Los lenguajes de marcas contienen información
Existen diferentes lenguajes
Cada uno de ellos tiene unas reglas de sintaxsis
No son muy complejos 
Dado que los lenguajes de marcas son para documentos es comun que la informacion se guarde en documentos
    Los lenguajes de marcaas nos ayudan en dos cosas:
    1. - Guardar documentos
    2. - Crear web/ crear interfaces web
    Por ejemplo markdown nos sirve para documento tipo word
    XML y JSON nos sirve para documentos complejos y anidados
    Anidados en programacion se entiende cuando una cosa esta dentro de otra






