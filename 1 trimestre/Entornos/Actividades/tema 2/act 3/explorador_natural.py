# Definir una función para contar árboles en un sendero
def contar_arboles(sendero):
    return len(sendero)

# Ejemplo de uso de la función
sendero = ['árbol1', 'árbol2', 'árbol3']
arboles_contados = contar_arboles(sendero)
print(f'Número de árboles en el sendero: {arboles_contados}')
